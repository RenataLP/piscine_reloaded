int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int s;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		s = s1[i] - s2[i];
		if (s == 0)
			i++;
		else
			return (s);
	}
	return (0);
}
